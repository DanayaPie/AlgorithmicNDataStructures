package syntax.Java.DataStructure.LinkedList.DoublyLinkedListImplementation;

public class DoublyLinkedListNode {

    int data;
    DoublyLinkedListNode next;
    DoublyLinkedListNode prev;

    public DoublyLinkedListNode(int data) {
        this.data = data;
        this.next = null;
        this.prev = null;
    }
}
